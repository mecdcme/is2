package it.istat.is2.workflow.domain;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.Table;

import it.istat.is2.workflow.domain.common.AbstractDomainObject;
import lombok.Getter;
import lombok.Setter;

@Getter
@Setter

@Entity
@Table(name = "IS2_CLS_DATA_TYPE")
public class DataTypeCls extends AbstractDomainObject implements Serializable {

    private static final long serialVersionUID = 1L;

    public DataTypeCls() {
        super();
    }

    public DataTypeCls(Long id) {
        super();
        this.id = id;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        DataTypeCls other = (DataTypeCls) obj;
        if (this.id == null) {
            if (other.id != null)
                return false;
        } else if (!this.id.equals(other.id))
            return false;
        return true;
    }


    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((this.id == null) ? 0 : this.id.hashCode());
        return result;
    }

}
